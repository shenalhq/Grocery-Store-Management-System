    private void jloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jloginActionPerformed
        String username = jusername.getText().trim();
        String password = new String(jpassword.getPassword());
        
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter both username and password");
            return;
        }
        
        UserManager userManager = new UserManager();
        User user = userManager.authenticateUser(username, password);
        
        if (user == null) {
            JOptionPane.showMessageDialog(this, "Invalid username or password");
            return;
        }
        
        UserRole userRole;
        if (user.getRole().equals("SalesAssistant")) {
            userRole = new SalesAssistant(user);
        } else if (user.getRole().equals("StoreManager")) {
            userRole = new StoreManager(user);
        } else {
            JOptionPane.showMessageDialog(this, "Invalid user role");
            return;
        }
        
        this.dispose();
        userRole.loginAction();
    }//GEN-LAST:event_jloginActionPerformed

